            <p class="description"><b>TEXTO IV:</b> <br>
                Mudanças sutis nas informações às quais somos expostos podem transformar nosso comportamento. A redes
                têm selecionado as notícias sob títulos chamativos como “trending topics” ou critérios como
                “relevância”. Mas nós praticamente não sabemos como isso tudo é filtrado. Quanto mais informações
