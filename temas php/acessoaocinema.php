            <p class="description"><b>TEXTO IV:</b> <br>
                O Brasil já teve um parque exibidor vigoroso e descentralizado: quase 3 300 salas em 1975, uma para cada
                30 000 habitantes, 80% em cidades do interior. Desde então, o país mudou. Quase 120 milhões de pessoas a
                mais passaram a viver nas cidades. A urbanização acelerada, a falta de investimentos em infraestrutura
                urbana, a baixa capitalização das empresas exibidoras, as mudanças tecnológicas, entre outros fatores,
                alteraram a geografia do cinema. Em 1997, chegamos a pouco mais de 1 000 salas. Com a expansão dos
                shopping centers, a atividade de exibição se reorganizou. O número de cinemas duplicou, até chegar às
                atuais 2 200 salas. Esse crescimento, porém, além de insuficiente (o Brasil é apenas o 60º país na
                relação habitantes por sala), ocorreu de forma concentrada. Foram privilegiadas as áreas de renda mais
                alta das grandes cidades. Populações inteiras foram excluídas do universo do cinema ou continuam mal
                atendidas: o Norte e o Nordeste, as periferias urbanas, as cidades pequenas e médias do interior.</p>
